bool TfrmName::flush(char pos)
{
    show->Lines->Clear();
    if (surname->Text.IsEmpty())
    {
    	return FALSE;
    }
    // update memo
    switch (pos)
    {
    	case 's':
            break;
    	case 'm':
            for (int i = 0; i < STROKE_MAXIMUM; i++)
            {
                if (AI(getStrokeByWord(surname->Text), getStrokeByWord(middlename->Text) + i))
                {
                    TStringList *tsl = getWordByStroke(i);
                    for (int j = 0; j < tsl->Count; j++)
                    {
                        show->Lines->Add(surname->Text + middlename->Text + tsl->Strings[j]);
                    }
                }
            }
            break;
    	case 'f':
            for (int i = 0; i < STROKE_MAXIMUM; i++)
            {
                if (AI(getStrokeByWord(surname->Text), i + getStrokeByWord(firstname->Text)))
                {
                    TStringList *tsl = getWordByStroke(i);
                    for (int j = 0; j < tsl->Count; j++)
                    {
                        show->Lines->Add(surname->Text + tsl->Strings[j] + firstname->Text);
                    }
                }
            }
	    break;
	default:
	    return FALSE;
    }
    show->Lines->SaveToFile(OUTPUT_FILE_NAME);
    return TRUE;
}

TList* TfrmName::assign_CSV(String filename)
{
    TList *tl = new TList;
    TStringList *tsl = new TStringList;
    try
    {
        tsl->LoadFromFile(filename);
    }
    catch (const Exception &e)
    {
        goto error;
    }
    for (int i = 0; i < tsl->Count; i++)
    {
        Pictograph *wp = new Pictograph;
        wp->word = tsl->Strings[i].SubString(0, 2);
        wp->strokes = tsl->Strings[i].SubString(4, 2).ToInt();  // 4 not 3?
        tl->Add(wp);
    }
    return tl;
error:
    return NULL;
}

TStringList* TfrmName::getWordByStroke(int strokes)
{
    TStringList *tsl = new TStringList;
    for (int i = 0; i < dictP->Count; i++)
    {
        if (((Pictograph*)dictP->Items[i])->strokes == strokes)
        {
            tsl->Add(((Pictograph*)dictP->Items[i])->word);
        }
    }
    if (0 == strokes)
    {
        tsl->Add("");
    }
    return tsl;
}

int TfrmName::getStrokeByWord(String word)
{
    if (word.Length() == 4)
    {
        return getStrokeByWord(word.SubString(1, 2)) + getStrokeByWord(word.SubString(3, 2));  // TO_DO
    }
    if (word == "")  // ("" == word) is error
        return 0;
    for (int i = 0; i < dictP->Count; i++)
    {
        if (((Pictograph*)dictP->Items[i])->word == word)
        {
            return ((Pictograph*)dictP->Items[i])->strokes;
        }
    }
    //ShowMessage("quit for " + word);
    PostQuitMessage(0);  // cruel
    return -1;
}

