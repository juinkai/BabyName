void __fastcall TfrmName::FormCreate(TObject *Sender)
{
    // show whatever information available to users
    Caption = "Pick a baby name v2.0.0.0 by " + String(AUTHOR);
    // init word list
    dictP = assign_CSV("dict.csv");
    if(dictP == NULL) PostQuitMessage(0);
    // clear all fields
    surname->Items->Clear();
    middlename->Items->Clear();
    firstname->Items->Clear();
    show->Lines->Clear();
    // init surname
    if (FileExists("surname.txt"))
    {
        surname->Items->LoadFromFile("surname.txt");
    }
}
