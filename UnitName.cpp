void __fastcall TfrmName::surnameChange(TObject *Sender)
{
    TStringList *comoList = new TStringList;
    for (int i = 0; i < dictP->Count; i++)
    {
        comoList->Add(((Pictograph*)dictP->Items[i])->word);
    }
    // prepare middlename list
    middlename->Items->Assign(comoList);
    // switch to middlename
    middlename->SetFocus();
    // allow empty string not NULL value
    comoList->Append("");
    // prepare firstname list
    firstname->Items->Assign(comoList);
    // final
    flush('s');
}
